<script>
export default {
  name: "EmitChildComponent",
  emits: ['clickChildButton'],
  methods: {
    clickButton() {
      // 이벤트를 부모에게 보내야합니다.
      //emit
      this.$emit('clickChildButton', '자식 emit 이벤트 발생');
    },
  }
}
</script>

<template>
  <button @click="clickButton">자식이 만든 버튼입니다.</button>
</template>

